export function findCentroid(countryName: string): CountryCentroid | null {
  if (!countryName) return null;

  const normalized = countryName.trim().toLowerCase().replace(/^the /, '');

  // Check alias first
  const alias = COUNTRY_ALIASES[normalized];
  if (alias) {
    return COUNTRY_CENTROIDS.find(c => c.name === alias) || null;
  }

  // Direct match
  const direct = COUNTRY_CENTROIDS.find(
    c => c.name.toLowerCase() === normalized
  );
  if (direct) return direct;

  // Partial match
  return COUNTRY_CENTROIDS.find(
    c => c.name.toLowerCase().includes(normalized) || normalized.includes(c.name.toLowerCase())
  ) || null;
}
